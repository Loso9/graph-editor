package com.example.editorgrafov.enums;

/**
 * Enum for options in File tab in menu
 */
public enum Action {
    /**
     * new action -- new window
     */
    NEW,
    /**
     * open action -- open window
     */
    OPEN,
    /**
     * exit action -- exit window
     */
    EXIT,
    /**
     * save action -- save session to current file
     */
    SAVE,
    /**
     * save as action -- save session to file chosen by user
     */
    SAVEAS;
}
