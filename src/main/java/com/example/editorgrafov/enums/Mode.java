package com.example.editorgrafov.enums;

/**
 * Enum for options in Mode tab of menu
 */
public enum Mode {
    /**
     * insert mode - adding vertices allowed
     */
    INSERT,
    /**
     * delete mode - deleting vertices allowed
     */
    DELETE,
    /**
     * add edge mode - adding edges allowed
     */
    ADDEDGE,
    /**
     * remove edge mode - removing edges allowed
     */
    REMOVEEDGE;
}
